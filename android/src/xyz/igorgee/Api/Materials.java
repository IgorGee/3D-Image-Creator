    Materials(int materialId, String title, String swatch) {
        this.materialId = materialId;
        this.title = title;
        this.swatch = swatch;
    }

    public int getMaterialId() {
        return materialId;
    }

    public String getSwatch() {
        return swatch;
    }

    public Materials getMaterialById(int id) {
        for (Materials material : Materials.values()) {
            if (material.getMaterialId() == id) {
                return material;
            }
        }
        throw new IllegalArgumentException("That is not a valid material id.");
    }

    @Override
    public String toString() {
        return title;
    }
